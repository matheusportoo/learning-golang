package main

import "fmt"

func main() {
	// String - golang
	fmt.Println("go" + "lang")

	// Integer - 2
	fmt.Println("1+1 =", 1+1)

	// Float - 2.3333333333333335
	fmt.Println("7.0/3.0 =", 7.0/3.0)

	// Boolean
	fmt.Println(true && false) // false
	fmt.Println(true || false) // true
	fmt.Println(!true)         // false
}
